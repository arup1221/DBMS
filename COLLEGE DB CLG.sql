CREATE PROCEDURE AVGMARKS()
BEGIN
    DECLARE C_A INT;
    DECLARE C_B INT;
    DECLARE C_C INT;
    DECLARE C_SUM INT;
    DECLARE C_AVG INT;
    DECLARE C_USN CHAR(10);
    DECLARE done INT DEFAULT FALSE;
    
    DECLARE C_IAMARKS CURSOR FOR
        SELECT GREATEST(TEST1, TEST2), GREATEST(TEST2, TEST3), GREATEST(TEST1, TEST3), USN
        FROM IAMARKS
        WHERE FINALIA IS NULL;

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = TRUE;

    OPEN C_IAMARKS;
    myLoop: LOOP
        FETCH C_IAMARKS INTO C_A, C_B, C_C, C_USN;

        IF done THEN
            LEAVE myLoop;
        END IF;

        IF C_A != C_B THEN
            SET C_SUM = C_A + C_B;
        ELSE
            SET C_SUM = C_A + C_C;
        END IF;

        SET C_AVG = C_SUM / 2;

        UPDATE IAMARKS SET FINALIA = C_AVG WHERE USN = C_USN;
    END LOOP myLoop;

    CLOSE C_IAMARKS;
END #
DELIMITER ;
 
