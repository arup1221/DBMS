
CREATE DATABASE COLLEGE_DATABASE;
USE COLLEGE_DATABASE;

CREATE TABLE STUDENT 
( 
USN CHAR(10),
SNAME VARCHAR(20), 
ADDRESS VARCHAR(25), 
PHONE BIGINT, 
GENDER CHAR,
CONSTRAINT PK_A PRIMARY KEY(USN)
);

CREATE TABLE SEMSEC 
( 
SSID CHAR(2),
SEM INT, 
SEC CHAR,
CONSTRAINT PK_B PRIMARY KEY(SSID), 
CONSTRAINT C CHECK(SEM BETWEEN 1 AND 8)
);

CREATE TABLE CLASS 
( 
USN CHAR(10),
SSID CHAR(2),
CONSTRAINT PK_D PRIMARY KEY(USN,SSID),
CONSTRAINT FK_E FOREIGN KEY(USN) REFERENCES STUDENT(USN) ON DELETE 
CASCADE,
CONSTRAINT FK_F FOREIGN KEY(SSID) REFERENCES SEMSEC(SSID) ON DELETE 
CASCADE
);

CREATE TABLE SUBJECT
( 
SUBCODE VARCHAR(7), 
TITLE VARCHAR(20), 
SEM INT, 
CREDITS INT,
CONSTRAINT PK_G PRIMARY KEY(SUBCODE)
);

CREATE TABLE IAMARKS 
( 
USN CHAR(10),
SUBCODE VARCHAR(7), 
SSID CHAR(2),
TEST1 INT, 
TEST2 INT, 
TEST3 INT, 
FINALIA INT,
CONSTRAINT PK_H PRIMARY KEY(USN,SUBCODE,SSID),
CONSTRAINT FK_Z FOREIGN KEY(USN) REFERENCES STUDENT(USN) ON DELETE 
CASCADE,
CONSTRAINT FK_Y FOREIGN KEY(SSID) REFERENCES SEMSEC(SSID) ON DELETE 
CASCADE,
CONSTRAINT FK_X FOREIGN KEY(SUBCODE) REFERENCES SUBJECT(SUBCODE) ON 
DELETE CASCADE
);


